public class halfpyramid {
    public static void main(String args[]){
        
             for(int line = 1 ; line <= 4 ; line ++){
                for (int counter = 1 ; counter <= line ; counter ++ ){
                    System.out.print(counter);
                }
                System.out.println();
             }
            
    }
}
